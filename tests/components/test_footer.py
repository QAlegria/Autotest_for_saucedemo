def check_footer_on_page(page_with_footer):
    page_with_footer.footer.twitter_is_displayed()
    page_with_footer.footer.facebook_is_displayed()
    page_with_footer.footer.linkedin_is_displayed()
    page_with_footer.footer.terms_footer_is_displayed()
    page_with_footer.footer.click_and_check_twitter_is_opened()
    page_with_footer.footer.click_and_check_facebook_is_opened()
    page_with_footer.footer.click_and_check_linkedin_is_opened()